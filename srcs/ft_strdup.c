#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*tmp;

	tmp = (char *)malloc(sizeof(char) * (ft_strlen(src) + 1));
	if (!tmp)
		return (NULL);
	i = 0;
	while (src[i])
	{
		tmp[i] = src[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
