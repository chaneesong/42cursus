#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_list	*stack;

	if (argc < 2)
		print_error();
	stack = parse_argv(argv);
	while(stack->a_top)
	{
		printf("val = %d\n", pop_a(&stack)->value);
	}
	return (0);
}
