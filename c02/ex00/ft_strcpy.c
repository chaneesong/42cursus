char	*ft_strcpy(char *dest, char *src)
{
	int	cnt;

	cnt = 0;
	while (*(src + cnt))
	{
		*(dest + cnt) = *(src + cnt);
		cnt++;
	}
	*(dest + cnt) = '\0';
	return (src);
}
