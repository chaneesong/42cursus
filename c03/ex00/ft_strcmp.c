int	ft_strcmp(char *s1, char *s2)
{
	int	cnt;

	if (!*s1 && !*s2)
		return (0);
	cnt = 0;
	if (!*s1)
		return (-(*s2));
	if (!*s2)
		return (*s1);
	while (*(s1 + cnt) && *(s2 + cnt))
	{
		if (*(s1 + cnt) != *(s2 + cnt))
			break ;
		cnt++;
	}
	return (*(s1 + cnt) - *(s2 + cnt));
}
