int	ft_strcmp(char *s1, char *s2)
{
	int	cnt;

	cnt = 0;
	while (*(s1 + cnt))
	{
		if (*(s1 + cnt) > *(s2 + cnt))
			return (1);
		else if (*(s1 + cnt) < *(s2 + cnt))
			return (-1);
		cnt++;
	}
	if (*(s2 + cnt))
		return (1);
	return (0);
}
